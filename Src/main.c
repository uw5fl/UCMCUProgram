void TuneTimers(int Period)
{	
	//Lengths of main output pulses for each channel
	htim1.Instance->CCR3 = Period + 120;
	htim2.Instance->CCR1 = htim5.Instance->CCR2 = Period;
	htim3.Instance->CCR1 = htim4.Instance->CCR2 = 4560 - Period;
	
	//Synchronization tune-up for timers 3 and 4
	htim2.Instance->CCR2 = Period + 102;
	htim5.Instance->CCR4 = Period + 102;
}

/*
ButtonIndex: a value from 0 to 7
DeltaDutyCycle: a value added to CurrentDutyCycle to be returned by this subroutine.
*/
int ButtonOnHandler (int ButtonIndex, int CurrentDutyCycle, int DeltaDutyCycle)
{
		//Kept pressed - perform action upon being kept pressed
		if (ButtonNoiseRejectorCounters[0] == BUTTON_ON_DELAY)
		{
			return CurrentDutyCycle + DeltaDutyCycle;
		}
		//Pressed just now - start upcounting to BUTTON_ON_DELAY
		else if (ButtonNoiseRejectorCounters[0] < BUTTON_ON_DELAY && ButtonNoiseRejectorCounters[0] >= BUTTON_OFF_DELAY)
		{
			ButtonNoiseRejectorCounters[0] += 1;
		}
		//Error, reinitialization required, next tick will be considered as "Pressed just now"
		else ButtonNoiseRejectorCounters[0] = BUTTON_OFF_DELAY;
		return CurrentDutyCycle;
}

void ButtonOffHandler (int ButtonIndex)
{
		//Kept released - perform action upon being kept released
		if (ButtonNoiseRejectorCounters[0] == BUTTON_OFF_DELAY)
		{
			
		}
		//Released just now - start downcounting to BUTTON_OFF_DELAY
		else if (ButtonNoiseRejectorCounters[0] <= BUTTON_ON_DELAY && ButtonNoiseRejectorCounters[0] > BUTTON_OFF_DELAY)
		{
			ButtonNoiseRejectorCounters[0] -= 1;
		}
		//Error, reinitialization required
		else ButtonNoiseRejectorCounters[0] = BUTTON_OFF_DELAY;
}

void AnalyzeDutyCycleSetter()
{
	int Temp = 300;

	if (HAL_GPIO_ReadPin (GPIOA, GPIO_PIN_2))
	{
		Temp = ButtonOnHandler (0, Temp, 200);
	}
	else
	{
		ButtonOffHandler(0);
	}

	if (HAL_GPIO_ReadPin (GPIOA, GPIO_PIN_3))
	{
		Temp = ButtonOnHandler (1, Temp, 200);
	}
	else
	{
		ButtonOffHandler(1);
	}
	if (HAL_GPIO_ReadPin (GPIOA, GPIO_PIN_4))
	{
		Temp = ButtonOnHandler (2, Temp, 200);
	}
	else
	{
		ButtonOffHandler(2);
	}
	if (HAL_GPIO_ReadPin (GPIOA, GPIO_PIN_7))
	{
		Temp = ButtonOnHandler (3, Temp, 200);
	}
	else
	{
		ButtonOffHandler(3);
	}
	if (HAL_GPIO_ReadPin (GPIOA, GPIO_PIN_8))
	{
		Temp = ButtonOnHandler (4, Temp, 200);
	}
	else
	{
		ButtonOffHandler(4);
	}
	if (HAL_GPIO_ReadPin (GPIOA, GPIO_PIN_9))
	{
		Temp = ButtonOnHandler (5, Temp, 200);
	}
	else
	{
		ButtonOffHandler(5);
	}
	if (HAL_GPIO_ReadPin (GPIOA, GPIO_PIN_11))
	{
		Temp = ButtonOnHandler (6, Temp, 200);
	}
	else
	{
		ButtonOffHandler(6);
	}
	if (HAL_GPIO_ReadPin (GPIOA, GPIO_PIN_12))
	{
		Temp = ButtonOnHandler (7, Temp, 200);
	}
	else
	{
		ButtonOffHandler(7);
	}
	
	TuneTimers(Temp);
}

